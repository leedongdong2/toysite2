package vo;

public class AboutImageVo {
	public int serial;
	public String imgName;
	public String uploadDate;
	public int imgWidth;
	public int imgHeight;
	public int imgZindex;
	public int imgTop;
	public int imgLeft;
	
	public int getSerial() {
		return serial;
	}
	public void setSerial(int serial) {
		this.serial = serial;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getUploadDate() {
		return uploadDate;
	}
	public void setUploadDate(String uploadDate) {
		this.uploadDate = uploadDate;
	}
	public int getImgWidth() {
		return imgWidth;
	}
	public void setImgWidth(int imgWidth) {
		this.imgWidth = imgWidth;
	}
	public int getImgHeight() {
		return imgHeight;
	}
	public void setImgHeight(int imgHeight) {
		this.imgHeight = imgHeight;
	}
	public int getImgZindex() {
		return imgZindex;
	}
	public void setImgZindex(int imgZindex) {
		this.imgZindex = imgZindex;
	}
	public int getImgTop() {
		return imgTop;
	}
	public void setImgTop(int imgTop) {
		this.imgTop = imgTop;
	}
	public int getImgLeft() {
		return imgLeft;
	}
	public void setImgLeft(int imgLeft) {
		this.imgLeft = imgLeft;
	}
}
